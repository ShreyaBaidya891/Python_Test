#append()
num1 = [1,2,3,4]
num1.append(5)
print(num1)

num1 =[10,11,12,13,14,15]
num2 =[16,17,18,19,20,21]
num1.append(num2)
print(num1)

#clear()
num1 = [100,200,300,400,500]
num1.clear()
print(num1)

#copy()
num1 = [10,11,12,13,14,15,16]
x = num1.copy()
print(x)

#count()
num1 = [100,200,500,800,1000,600,500,200,500,200]
x = num1.count(200)
print(x)

#extend()
num1 = [100,200,300,400,500]
num2 = [600,700,800,900,1000]
num1.extend(num2)
print(num1)

#index()
num1 = [200,300,400,500,600]
x = num1.index(500)
print(x)

#insert()
num1 = [1000,3000,4000,5000,6000]
num1.insert(1,2000)
print(num1)

#pop()
num1 = [1,2,3,4,5]
num1.pop(1)
print(num1)

#remove()
num1 = [1,5,8,10,50,25,40]
num1.remove(25)
print(num1)

#reverse()
num1 = [1,50,25,40,15,60]
num1.reverse()
print(num1)

#sort()
num2 = [10,70,50,60,30,20,40]
num2.sort()
print(num2)



